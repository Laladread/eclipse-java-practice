
public class TwoDimArr {

	public static void main(String[] args) {
		String[][] coupleArray = new String[4][2];
		coupleArray[0][0] = "John";
		coupleArray[0][1] = "Rose";
		coupleArray[1][0] = "Peter";
		coupleArray[1][1] = "Abigail";
		coupleArray[2][0] = "Robert";
		coupleArray[2][1] = "Josephine";
		coupleArray[3][0] = "Timothy";
		coupleArray[3][1] = "Angelina";
		
		System.out.println();

	}

}
